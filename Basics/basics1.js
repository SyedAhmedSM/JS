/* You and your friend are baking cupcakes for a party. You've filled up two trays, each with a certain
number of cupcakes. You want to know the total number of cupcakes you have before you frost
them.

Challenge

Create a function that adds two numbers (representing cupcakes on each tray) and returns the
total. */

function totalCupcakes(trayOne, trayTwo) {
  // Return the sum of trayOne and trayTwo
  return trayOne + trayTwo;
}

//sample usage
console.log(totalCupcakes(12, 24)); //36
console.log(totalCupcakes(5, 5)); //10
